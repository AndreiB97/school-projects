public final class Point {

    ////////////////////////
    // Variables

    private final int x;
    private final int y;

    // End variables
    ////////////////////////



    ////////////////////////
    // Methods

    public int getX() {
        return x;
    }

    public int getY() {
        return y;

    }

    public Point(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    // End methods
    ////////////////////////
}
